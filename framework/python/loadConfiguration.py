def get_refresh_token(global_config_file):

  # Read the auth-type from the global config file, if it is set to SCIM, then we need to get the refresh token from the credentials file
  with open(global_config_file, 'r') as file:
    global_config = yaml.safe_load(file)
    global_config = global_config.get("global-config", {})
    auth_type = global_config.get("auth-type", None)
    viya_cli_profile = global_config.get("viya-cli-profile", None)
  
  if not auth_type:
    logging.error("auth-type is not set in the global config file.")
    sys.exit(1)

  if str(auth_type).upper() != "SCIM":
    logging.error("No refresh token is required for this auth-type.")
    return None
  
  if not viya_cli_profile:
    logging.error("viya-cli-profile is not set in the global config file.")
    sys.exit(1)

  # Read the contents of ~/.sas/credentials.json
  credentials_file_path = os.path.expanduser("~/.sas/credentials.json")
  if not os.path.exists(credentials_file_path):
    logging.error(f"Credentials file not found at {credentials_file_path}.")
    sys.exit(1)

  with open(credentials_file_path, 'r') as file:
    credentials = json.load(file)
    if not credentials:
      logging.error(f"Error: Credentials file is empty at {credentials_file_path}.")
      sys.exit(1)

    #Get the "Default"."refresh_token" value from the json file
    logging.debug(f"Reading refresh token from credentials file at {credentials_file_path} for profile: {viya_cli_profile}")
    profile_contents = credentials.get(viya_cli_profile, {}) #.get("access_token", None)
    refresh_token = profile_contents.get("refresh-token")

    if not refresh_token:
      logging.error(f"Error: Refresh token not found for key: {viya_cli_profile}.")
      sys.exit(1)

    logging.debug(f"Found refresh token for profile {viya_cli_profile}: {refresh_token}")  

    return refresh_token

  return credentials.get("refresh_token", None)
    
def merge_yaml_files(base_file, merge_file, defaults_file, parent_node):
  # Define merging strategy.
  merger = Merger(
    [(dict, ["merge"]), (list, ["append"])],  # Merge dictionaries, append lists
    ["override"],  # Fallback strategy
    ["override"]  # Conflict resolution strategy
  )

  # Check if an optional file is provided and exists
  optional_file = args.optional_file if hasattr(args, 'optional_file') else None
  if optional_file and os.path.exists(optional_file):
    with open(optional_file, 'r') as file4:
      optional_file_content = yaml.safe_load(file4)
      optional_file_content = optional_file_content.get("optional-config", {})
      # Merge the optional file content
      base_file = merger.merge(base_file, optional_file_content)

  with open(base_file, 'r') as file1:
    base_file = yaml.safe_load(file1)
    base_file = base_file.get("global-config", {})

  with open(merge_file, 'r') as file2:
    scenario_file = yaml.safe_load(file2)  
    scenario_file = scenario_file.get(parent_node, {})

  merged_config = merger.merge(base_file, scenario_file)

  if defaults_file:
    logging.info(f"Merging defaults file: {defaults_file}")    
    with open(defaults_file, 'r') as file10:
      defaults_file_contents = yaml.safe_load(file10)  
      merged_config = merger.merge(merged_config, defaults_file_contents)
  
  return merged_config
  
def write_config_variables(output_file, env_vars):
  for key, value in env_vars.items():
    camel_case_key = ''.join(word.capitalize() if i != 0 else word for i, word in enumerate(key.split('-')))
    json_value = json.dumps(value)
    output_file.write(f"export {camel_case_key}=$(jq -r '.' <<< '{json_value}')\n")     

#Run the function
logging.info("Loading configuration and setting environment variables...")
set_env_from_yaml(args)
