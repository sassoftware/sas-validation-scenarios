# Function to load YAML file and set environment variables
def set_env_from_yaml(args):

  global_config_file = args.global_config
  workload_config_file = args.workload_config
  scenario_config_file = args.scenario_config
  framework_defaults_file = args.framework_defaults

  if workload_config_file and not os.path.exists(workload_config_file):
    logging.error(f"File {workload_config_file} does not exist")
    sys.exit(1)

  if scenario_config_file and not os.path.exists(scenario_config_file):
    logging.error(f"File {scenario_config_file} does not exist")
    sys.exit(1)

  if framework_defaults_file and not os.path.exists(framework_defaults_file):
    logging.error(f"File {framework_defaults_file} does not exist")
    sys.exit(1)

  if not os.path.exists(global_config_file):
    logging.error(f"File {global_config_file} does not exist")
    sys.exit(1)

  if workload_config_file:
    logging.info(f"Merging configuration files: {global_config_file} and {workload_config_file}")
    config_file_contents = merge_yaml_files(global_config_file, workload_config_file, framework_defaults_file, 'workload-definition')  
  elif scenario_config_file:
    logging.info(f"Merging configuration files: {global_config_file} and {scenario_config_file}")
    config_file_contents = merge_yaml_files(global_config_file, scenario_config_file, framework_defaults_file, 'scenario-config')

  output_file_path = os.path.join(args.build_path,  'setVariables.sh')
  with open(output_file_path, 'w') as output_file:

    logging.info(f"Writing global environment variables to {output_file_path}")
    output_file.write(f"#!/usr/bin/env bash \n\n")
    output_file.write(f"## NOTE: This file is generated by loadConfiguration.py and will be overwritten. ##\n\n")

    framework_scripts_location_full_path = os.getenv("frameworkScriptsLocationFullPath")
    output_file.write(f"export frameworkScriptsLocationFullPath={framework_scripts_location_full_path}\n")

    scenarios_location_full_path = os.getenv("scenariosLocationFullPath")
    output_file.write(f"export scenariosLocationFullPath={scenarios_location_full_path}\n")

    users_file_full_path = args.users_file
    output_file.write(f"export usersFileFullPath={users_file_full_path}\n")

    ## TODO This is part of the reource setup scripting and yet available
    # auth_refresh_token = get_refresh_token(global_config_file)
    # output_file.write(f"export authRefreshToken={auth_refresh_token}\n\n")

    # Get the workloads-global-config environment variables
    write_config_variables(output_file, config_file_contents)    

